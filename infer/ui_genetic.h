#define G_MUTATION_PROB  (0.1f)
#define G_CROSSOVER_PROB (0.5f)
#define G_WIDTH  (64)
#define G_HEIGHT (64)
#define G_GENE_LENGTH (G_WIDTH * G_HEIGHT)
#define G_POP_SIZE (200)

typedef struct {
    float gene[G_GENE_LENGTH];
} G_Individual;

typedef struct {
    float ref[G_GENE_LENGTH];
    G_Individual population[G_POP_SIZE];
    float fitness[G_POP_SIZE];
    float total_fitness;
    int best_id;
    float best_fitness;
} G_Eden;

static G_Eden s_eden_r;
static G_Eden s_eden_g;
static G_Eden s_eden_b;
static G_Individual s_g_new_population[G_POP_SIZE];
static uint64_t s_g_rng_state = 0;
static uint32_t s_g_generation = 0;
static int32_t s_g_initialized = 0;

static inline float g_random_float(void) {
    return random_f32(&s_g_rng_state);
}

static inline int g_random_int(int n) {
    return (int)(random_u32(&s_g_rng_state) % (uint32_t)n);
}

static float g_mse(const float *gene, const float *ref) {
    float mse = 0.0f;
    for (int i = 0; i < G_GENE_LENGTH; i++) {
        float diff = ref[i] - gene[i];
        mse += diff * diff;
    }
    return mse;
}

static void g_individual_init(G_Individual *ind) {
    for (int i = 0; i < G_GENE_LENGTH; i++) {
        ind->gene[i] = (float)g_random_int(255);
    }
}

static float g_individual_decode(const G_Individual *ind, const float *ref) {
    float m = g_mse(ind->gene, ref);
    if (m <= 0.0f) {
        return 1e30f;
    }
    return 1.0f / m;
}

static void g_individual_mutate(G_Individual *ind) {
    int pos = g_random_int(G_GENE_LENGTH);
    ind->gene[pos] = g_random_float();
}

static void g_individual_crossover(const G_Individual *a, const G_Individual *b, G_Individual *child) {
    int p1 = g_random_int(G_GENE_LENGTH);
    int p2 = g_random_int(G_GENE_LENGTH);
    int p3 = g_random_int(G_GENE_LENGTH);
    int p4 = g_random_int(G_GENE_LENGTH);

    int pos[4] = {p1, p2, p3, p4};
    // bubble sort
    for (int i = 0; i < 3; i++) {
        for (int j = 0; j < 3 - i; j++) {
            if (pos[j] > pos[j + 1]) {
                int tmp = pos[j];
                pos[j] = pos[j + 1];
                pos[j + 1] = tmp;
            }
        }
    }

    for (int i = 0; i < G_GENE_LENGTH; i++) {
        if ((i >= pos[0] && i <= pos[1]) || (i >= pos[2] && i <= pos[3])) {
            child->gene[i] = a->gene[i];
        } else {
            child->gene[i] = b->gene[i];
        }
    }
}

static void g_eden_evaluate(G_Eden *eden) {
    float max_value = 0.0f;
    int max_index = 0;
    float total = 0.0f;
    for (int i = 0; i < G_POP_SIZE; i++) {
        float fit = g_individual_decode(&eden->population[i], eden->ref);
        eden->fitness[i] = fit;
        total += fit;
        if (fit >= max_value) {
            max_value = fit;
            max_index = i;
        }
    }
    eden->total_fitness = total;
    eden->best_id = max_index;
    eden->best_fitness = max_value;
}

static int g_eden_roulette_wheel(G_Eden *eden) {
    float pointer = g_random_float();
    float position = 0.0f;
    for (int i = 0; i < G_POP_SIZE; i++) {
        position += eden->fitness[i] / eden->total_fitness;
        if (pointer <= position) {
            return i;
        }
    }
    return G_POP_SIZE - 1;
}

static void g_eden_next_generation(G_Eden *eden) {
    s_g_new_population[0] = eden->population[eden->best_id];

    int count = 1;
    while (count < G_POP_SIZE) {
        int p1 = g_eden_roulette_wheel(eden);
        G_Individual child;
        if (g_random_float() <= G_CROSSOVER_PROB) {
            int p2 = g_eden_roulette_wheel(eden);
            g_individual_crossover(&eden->population[p1], &eden->population[p2], &child);
        } else {
            child = eden->population[p1];
        }
        if (g_random_float() <= G_MUTATION_PROB) {
            g_individual_mutate(&child);
        }
        s_g_new_population[count++] = child;
    }

    for (int i = 0; i < G_POP_SIZE; i++) {
        eden->population[i] = s_g_new_population[i];
    }
}

static void g_eden_evolve(G_Eden *eden) {
    g_eden_evaluate(eden);
    g_eden_next_generation(eden);
}

static void g_eden_init(G_Eden *eden, const float *ref) {
    for (int i = 0; i < G_GENE_LENGTH; i++) {
        eden->ref[i] = ref[i];
    }
    for (int i = 0; i < G_POP_SIZE; i++) {
        g_individual_init(&eden->population[i]);
    }
    eden->total_fitness = 0.0f;
    eden->best_id = 0;
    eden->best_fitness = 0.0f;
    g_eden_evaluate(eden);
}

void ui_app_genetic_init(Key_Event *key_event, Global_State *global_state) {

    s_g_rng_state = global_state->timestamp;
    s_g_generation = 0;

    float ref_r[G_GENE_LENGTH];
    float ref_g[G_GENE_LENGTH];
    float ref_b[G_GENE_LENGTH];

    for (int y = 0; y < G_HEIGHT; y++) {
        for (int x = 0; x < G_WIDTH; x++) {
            int idx = y * G_WIDTH + x;
            int img_idx = (y * G_WIDTH + x) * 3;
            ref_r[idx] = (float)image_rgb888[img_idx];
            ref_g[idx] = (float)image_rgb888[img_idx + 1];
            ref_b[idx] = (float)image_rgb888[img_idx + 2];
        }
    }

    g_eden_init(&s_eden_r, ref_r);
    g_eden_init(&s_eden_g, ref_g);
    g_eden_init(&s_eden_b, ref_b);

    // JS: new Eden(200, matref); eden.Evolve();
    g_eden_evolve(&s_eden_r);
    g_eden_evolve(&s_eden_g);
    g_eden_evolve(&s_eden_b);

    s_g_initialized = 1;
}

